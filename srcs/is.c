#include "../includes/ft_printf.h"

char	is_flag(char c)
{
	return (c == '#' || c == '0' || c == '-' ||\
			c == '+' || c == ' ');
}

char	is_mod(char c)
{
	return (c == 'l' || c == 'h' );
}
